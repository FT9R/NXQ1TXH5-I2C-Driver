float ADC_LsbToVoltage(q15_t lsb)
{
    return lsb * VREF / pow(2, 12) / 1e3;
}

float ADC_TemperatureGet(q15_t lsb)
{
    static float ntcVoltage;
    float ntcResistance;
    float temperatureKelvin;

    ntcVoltage = 2 * ADC_LsbToVoltage(lsb);
    ntcResistance = 20e3 * ntcVoltage / (1 - 0.3 * ntcVoltage);
    temperatureKelvin = NTC_BETA / (log(ntcResistance / 1e5) + NTC_BETA / (25 + 273.15));
    // temperatureKelvin = 1 / (1 / 298.15 + 1 / NTC_BETA * log(ntcResistance / 1e5));

    return temperatureKelvin - 273.15;
}

float ADC_VinGet(q15_t lsb)
{
    float divVoltage;

    divVoltage = ADC_LsbToVoltage(lsb);

    return divVoltage / 51e3 * (332e3 + 51e3);
}
